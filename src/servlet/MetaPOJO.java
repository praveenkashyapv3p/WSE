package servlet;

public class MetaPOJO {
    int metaVmId;
    int metaRank;
    String metaUrl;
    Double metaScore;

    public int getMetaVmId() {
        return this.metaVmId;
    }

    public void setMetaVmId(int metaVmId) {
        this.metaVmId = metaVmId;
    }

    public int getMetaRank() {
        return this.metaRank;
    }

    public void setMetaRank(int metaRank) {
        this.metaRank = metaRank;
    }

    public String getMetaUrl() {
        return this.metaUrl;
    }

    public void setMetaUrl(String metaUrl) {
        this.metaUrl = metaUrl;
    }

    public Double getMetaScore() {
        return this.metaScore;
    }

    public void setMetaScore(Double metaScore) {
        this.metaScore = metaScore;
    }



}
